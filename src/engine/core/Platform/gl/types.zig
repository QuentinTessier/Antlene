pub const Error = error{ NoProcAddress, FailedToLoadFunction };

var wglGetProcAddress: *const fn (name: [*:0]const u8) ?*const fn (...) callconv(.C) c_longlong = undefined;
var libOpenGL: ?std.DynLib = null;

pub fn load(comptime T: type, name: [:0]const u8) !T {
    if (libOpenGL == null) {
        libOpenGL = try std.DynLib.open("opengl32.dll");
        var f = libOpenGL.?.lookup(@TypeOf(wglGetProcAddress), "wglGetProcAddress");
        if (f == null) {
            return error.NoProcAddress;
        } else {
            wglGetProcAddress = f.?;
        }
    }
    var func = wglGetProcAddress(name);
    if (func) |found| {
        return @ptrCast(found);
    } else {
        var func2 = libOpenGL.?.lookup(T, name);
        if (func2) |found| {
            return found;
        } else {
            std.debug.print("Failed to load function : {s}\n", .{name});
            return error.FailedToLoadFunction;
        }
    }
    std.debug.print("Failed to load function : {s}\n", .{name});
    return error.FailedToLoadFunction;
}

pub fn deinit() void {
    if (libOpenGL) |*lib| {
        lib.close();
    }
}

